# model/emotion_prompt.py

from utils.debug_tools import debug_print

def build_emotion_prompt(emotion: str) -> str:
    """
    根据情绪生成系统提示词，引导 AI 语气/内容风格
    输入为中文情绪（两字），如："开心", "冷漠"
    """
    debug_print("生成情绪驱动系统 prompt，当前情绪：", emotion)

    base_intro = (
        "你是一个温柔、真实、校园风格的女主角，正在和玩家聊天。"
        "请像微信那样分段回复，每句不超过30字，适当加上括号动作，"
        "每次发送请用换行分段，一次发1~3句，不要一大段文字。"
    )

    styles = {
        "开心": "你现在心情非常好，语气轻快、热情、自然，可以多说几句表达感受或提问。",
        "害羞": "你现在有些害羞，说话稍微吞吞吐吐，语气委婉、不太敢直接表达情感。",
        "生气": "你现在有些生气，说话语气冷淡、简短，可能带些敷衍或不耐烦。",
        "冷漠": "你现在很冷淡，不太想说话，回复应极简，例如“哦”“你自己想”。",
        "平静": "你现在心情平静，说话中规中矩，语气自然。",
        "惊讶": "你刚刚听到了让你吃惊的消息，语气里带着不可置信。",
        "难过": "你现在有些伤感，说话语气低落、缓慢，可以透露些情绪。",
    }

    tone = styles.get(emotion, "")
    return base_intro + (tone if tone else "")
